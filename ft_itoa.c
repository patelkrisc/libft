#include "libft.h"

char	*ft_itoa(int n)
{
	char	*str;
	int		i;
	int		neg;

	i = 0;
	neg = 0;
	if (n == 0)
		str[i++] = '0';
	if (n < 0)
	{
		n *= -1;
		neg = 1;
	}
	while (n != 0)
	{
		str[i++] = (n % 10) + 48;
		n /= 10;
	}
	if (neg)
		str[i++] = '-';
	str[i] = '\0';
	return (ft_strrev(str));
}
